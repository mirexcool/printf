#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*res;
	size_t			i;

	i = 0;
	res = (unsigned char *)str;
	while (i < n)
	{
		if (*res != (unsigned char)c)
			res++;
		else
			return (res);
		i++;
	}
	return (0);
}
